package Exercises;

/*
    Mostrar los numeros de 2 en dos, a partir del 50, hasta llegar al 100.
    Ejemplo: 50-52-54-56-58...100
*/

public class Exercise2 {
    public static void main(String[] args) {
        for (int i = 50; i <= 100 ; i +=2) {
            System.out.println(i);
        }
    }
}